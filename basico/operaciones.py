a = 10
b = 4

suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b  # división normal (decimal)
division_entera = a // b  # solo la parte entera
modulo = a % b  # residuo
potencia = a ** b  # a elevado a la b

print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("División entera:", division_entera)
print("Módulo:", modulo)
print("Potencia:", potencia)

